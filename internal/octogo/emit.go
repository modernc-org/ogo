package octogo // import "modernc.org/ogo/internal/ogo"

import (
	"fmt"
	"io"
	"slices"
	"strconv"
)

// p2Intrinsics maps exported functions of the builtin "p2" package to the
// flexcc / propeller2.h C intrinsics they wrap (the p2 mapping documented in
// CLAUDE.md's appendix). The call `p2.PinHigh(56)` emits `_pinh(56)`.
var p2Intrinsics = map[string]string{
	"PinHigh":      "_pinh",
	"PinLow":       "_pinl",
	"PinIn":        "_pinr",
	"WaitMs":       "_waitms",
	"AckPin":       "_akpin",
	"ReadPin":      "_rdpin",
	"WritePinMode": "_wrpin",
	"WritePinX":    "_wxpin",
	"WritePinY":    "_wypin",
}

// importIncludes maps an OctoGo import path to the C header it pulls in.
var importIncludes = map[string]string{
	"p2": "propeller2.h",
}

// EmitC writes C source for the built package pkg to w. It is the walking
// skeleton of the OctoGo C backend, grown to a first useful subset: `func
// main()` containing `for {}` loops and calls to the builtin p2 package
// (mapped to P2 intrinsics). Anything it does not yet understand produces an
// "emit:" error rather than wrong C, so the surface grows honestly.
//
// The traversal mirrors the checker (see it()/sourceFile/funcDecl in check.go):
// dispatch non-terminals on Node.sym, read terminals via File.tok/File.ch.
func EmitC(pkg *Package, w io.Writer) error {
	e := &emitter{w: w}
	e.pr("// Code generated by ogo. DO NOT EDIT.\n\n")

	for _, inc := range e.collectIncludes(pkg) {
		e.pr("#include <%s>\n", inc)
	}
	if e.included {
		e.pr("\n")
	}

	for _, f := range pkg.Files {
		e.f = f
		e.emitFileDecls(f.AST)
	}
	return e.err
}

type emitter struct {
	w        io.Writer
	f        *File // file currently being emitted, for token access
	indent   int
	included bool // an #include was emitted
	err      error
}

// pr writes formatted C, latching the first write error.
func (e *emitter) pr(format string, args ...any) {
	if e.err != nil {
		return
	}
	_, e.err = fmt.Fprintf(e.w, format, args...)
}

// fail latches the first "not yet implemented / unsupported" emit error.
func (e *emitter) fail(format string, args ...any) {
	if e.err == nil {
		e.err = fmt.Errorf("emit: "+format, args...)
	}
}

func (e *emitter) ind() {
	for i := 0; i < e.indent; i++ {
		e.pr("\t")
	}
}

// collectIncludes gathers the C headers pulled in by the package's imports,
// deduplicated and sorted.
func (e *emitter) collectIncludes(pkg *Package) []string {
	set := map[string]bool{}
	for _, f := range pkg.Files {
		e.f = f
		for n := range it(f.AST) {
			if n.sym != SourceFile {
				continue
			}
			for c := range it(n.ast) {
				if c.sym == ImportDecl {
					e.importIncludes(c.ast, set)
				}
			}
		}
	}
	out := make([]string, 0, len(set))
	for inc := range set {
		out = append(out, inc)
	}
	slices.Sort(out)
	e.included = len(out) != 0
	return out
}

func (e *emitter) importIncludes(ast []int32, set map[string]bool) {
	for n := range it(ast) {
		if n.sym != ImportSpec {
			continue
		}
		for c := range it(n.ast) {
			if c.sym != 0 || e.f.ch(c.tok) != STRING {
				continue
			}
			path, err := strconv.Unquote(e.f.tok(c.tok).Src())
			if err != nil {
				continue
			}
			if inc, ok := importIncludes[path]; ok {
				set[inc] = true
			} else {
				e.fail("no C header mapping for import %q", path)
			}
		}
	}
}

func (e *emitter) emitFileDecls(ast []int32) {
	for n := range it(ast) {
		if n.sym != SourceFile {
			continue
		}
		for c := range it(n.ast) {
			switch c.sym {
			case TopLevelDecl:
				e.emitTopLevelDecl(c.ast)
			case ImportDecl, 0:
				// Imports become #includes (collectIncludes); 0 = separators.
			default:
				e.fail("unsupported source-file element %v", c.sym)
			}
		}
	}
}

func (e *emitter) emitTopLevelDecl(ast []int32) {
	for n := range it(ast) {
		switch n.sym {
		case FuncDecl:
			e.emitFuncDecl(n.ast)
		default:
			e.fail("unsupported top-level declaration %v", n.sym)
		}
	}
}

func (e *emitter) emitFuncDecl(ast []int32) {
	var name string
	var body []int32
	hasBody := false
	for n := range it(ast) {
		switch n.sym {
		case 0:
			if e.f.ch(n.tok) == IDENT {
				name = e.f.tok(n.tok).Src()
			}
		case Receiver:
			e.fail("methods are not supported yet")
		case Signature:
			e.emitSignatureIsEmpty(n.ast)
		case Block:
			hasBody = true
			body = n.ast
		default:
			e.fail("unsupported in function declaration: %v", n.sym)
		}
	}

	// Walking skeleton: only `func main()` with an empty signature.
	if name != "main" {
		e.fail("only func main is supported yet, got func %q", name)
		return
	}
	if !hasBody {
		e.fail("func main must have a body")
		return
	}
	e.pr("int main(void) {\n")
	e.indent++
	e.emitBlockStmts(body)
	e.ind()
	e.pr("return 0;\n")
	e.indent--
	e.pr("}\n")
}

// emitSignatureIsEmpty asserts the signature has no parameters or results, the
// only shape supported by the skeleton (i.e. `func main()`).
func (e *emitter) emitSignatureIsEmpty(ast []int32) {
	for n := range it(ast) {
		switch n.sym {
		case 0:
			// LPAREN / RPAREN.
		case ParameterList:
			e.fail("function parameters are not supported yet")
		default:
			e.fail("function results are not supported yet (%v)", n.sym)
		}
	}
}

// emitBlockStmts emits the statements of a Block (skipping its braces).
func (e *emitter) emitBlockStmts(ast []int32) {
	for n := range it(ast) {
		switch n.sym {
		case 0:
			// LBRACE / RBRACE / SEMICOLON.
		case Statement:
			e.emitStatement(n.ast)
		default:
			e.fail("unsupported block element %v", n.sym)
		}
	}
}

func (e *emitter) emitStatement(ast []int32) {
	nodes := slices.Collect(it(ast))
	if len(nodes) == 0 {
		return // EmptyStatement
	}
	switch first := nodes[0]; {
	case first.sym == 0 && e.f.ch(first.tok) == FOR:
		e.emitFor(nodes)
	case first.sym == AssignHead:
		e.emitCallStmt(nodes)
	case first.sym == 0:
		e.fail("%v statement is not supported yet", e.f.ch(first.tok))
	default:
		e.fail("statement %v is not supported yet", first.sym)
	}
}

// emitFor handles `for { ... }` (infinite loop). Loop conditions and clauses
// are not supported yet.
func (e *emitter) emitFor(nodes []Node) {
	var body []int32
	for _, n := range nodes[1:] {
		switch n.sym {
		case Block:
			body = n.ast
		default:
			e.fail("for-loop conditions are not supported yet")
			return
		}
	}
	if body == nil {
		e.fail("for-loop without a body")
		return
	}
	e.ind()
	e.pr("for (;;) {\n")
	e.indent++
	e.emitBlockStmts(body)
	e.indent--
	e.ind()
	e.pr("}\n")
}

// emitCallStmt handles a qualified package-function call statement such as
// `p2.PinHigh(56)`, parsed as AssignHead Postfix{Selector CallSuffix}.
func (e *emitter) emitCallStmt(nodes []Node) {
	if len(nodes) != 2 || nodes[1].sym != Postfix {
		e.fail("only package-function call statements are supported yet")
		return
	}
	recv := e.soleIdent(nodes[0].ast) // e.g. "p2"
	postfix := slices.Collect(it(nodes[1].ast))
	if recv == "" || len(postfix) != 2 || postfix[0].sym != Selector || postfix[1].sym != CallSuffix {
		e.fail("only <pkg>.<Func>(args) call statements are supported yet")
		return
	}
	if recv != "p2" {
		e.fail("unknown package %q (only p2 is supported yet)", recv)
		return
	}
	method := e.soleIdent(postfix[0].ast) // ".PinHigh" -> "PinHigh"
	intr, ok := p2Intrinsics[method]
	if !ok {
		e.fail("unsupported p2 function %q", method)
		return
	}
	e.ind()
	e.pr("%s(", intr)
	e.emitCallArgs(postfix[1].ast)
	e.pr(");\n")
}

// soleIdent returns the single identifier of a subtree (used for an AssignHead
// bare identifier and a Selector's field name), or "" if the shape is richer.
func (e *emitter) soleIdent(ast []int32) string {
	name := ""
	for n := range it(ast) {
		if n.sym != 0 {
			continue
		}
		if e.f.ch(n.tok) == IDENT {
			if name != "" {
				return "" // more than one identifier: not a simple name
			}
			name = e.f.tok(n.tok).Src()
		}
	}
	return name
}

func (e *emitter) emitCallArgs(callSuffix []int32) {
	for n := range it(callSuffix) {
		if n.sym != ArgumentList {
			continue
		}
		first := true
		for a := range it(n.ast) {
			switch a.sym {
			case Expression:
				if !first {
					e.pr(", ")
				}
				first = false
				e.emitExpr(a.ast)
			case 0:
				// COMMA.
			default:
				e.fail("unsupported argument %v", a.sym)
			}
		}
	}
}

// emitExpr emits a value expression. Skeleton support: a bare int literal or
// identifier (descending the Expression/SimpleExpr/Term/UnaryExpr/Factor wrapper
// chain). Operators and richer factors are not supported yet.
//
// TODO: normalize Go literal syntax that C rejects (digit separators `1_000`,
// `0b`/`0o` prefixes) — currently the literal text is emitted verbatim, which is
// correct only for plain decimal integers.
func (e *emitter) emitExpr(ast []int32) {
	nodes := slices.Collect(it(ast))
	if len(nodes) != 1 {
		e.fail("operator expressions are not supported yet")
		return
	}
	switch n := nodes[0]; n.sym {
	case Expression, SimpleExpr, Term, UnaryExpr, Factor:
		e.emitExpr(n.ast)
	case 0:
		switch e.f.ch(n.tok) {
		case INT, IDENT:
			e.pr("%s", e.f.tok(n.tok).Src())
		default:
			e.fail("unsupported operand %v", e.f.ch(n.tok))
		}
	default:
		e.fail("unsupported expression %v", n.sym)
	}
}
