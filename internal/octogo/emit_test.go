// TestEmitCSliceCap pins the capacity field: a slice header is { ptr, len, cap }.
// Slicing an array a[low:high] sets cap = len(a) - low; re-slicing a slice sets
// cap = cap(base) - low, so a re-slice may extend the length back up to cap
// (s[:5] with len 2, cap 6 -> len 5). cap(x) reads the header (or the array bound).
// Verified: cap(a[2:4]) == 6, s[:5] -> len 5 / cap 6, cap(s[1:3]) == 5, cap(a) == 8.
