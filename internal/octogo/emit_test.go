// TestNormalizeIntLit unit-tests the integer-literal normalizer: underscores are
// stripped and Go's "0o"/"0O" octal prefix becomes a C bare-zero prefix, while
// decimal, hex, binary and legacy leading-zero octal keep their base.
func TestNormalizeIntLit(t *testing.T) {
	for _, tc := range []struct{ in, want string }{
		{"0", "0"},
		{"42", "42"},
		{"1_000_000", "1000000"},
		{"0xFF", "0xFF"},
		{"0Xf_f", "0Xff"},
		{"0b1010", "0b1010"},
		{"0B_1010", "0B1010"},
		{"0o17", "017"},
		{"0O755", "0755"},
		{"017", "017"},
		{"0o0", "00"},
	} {
		if got := normalizeIntLit(tc.in); got != tc.want {
			t.Errorf("normalizeIntLit(%q) = %q, want %q", tc.in, got, tc.want)
		}
	}
}

